package net.blackedin.registration.model;

public class Empresa {
	
	private String nome_empresa;
	private String CNPJ;
	private String email_empresa;
	private String razao_social;
	private String senha;
	private String porte_empresa;
	private String area_atuacao;
	private String modelo_trabalho;
	private String descricao;

	public String getNome_empresa() {
		return nome_empresa;
	}
	public void setNome_empresa(String nome_empresa) {
		this.nome_empresa = nome_empresa;
	}
	public String getCNPJ() {
		return CNPJ;
	}
	public void setCNPJ(String cNPJ) {
		CNPJ = cNPJ;
	}
	public String getEmail_empresa() {
		return email_empresa;
	}
	public void setEmail_empresa(String email_empresa) {
		this.email_empresa = email_empresa;
	}
	public String getRazao_social() {
		return razao_social;
	}
	public void setRazao_social(String razao_social) {
		this.razao_social = razao_social;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getPorte_empresa() {
		return porte_empresa;
	}
	public void setPorte_empresa(String porte_empresa) {
		this.porte_empresa = porte_empresa;
	}
	public String getArea_atuacao() {
		return area_atuacao;
	}
	public void setArea_atuacao(String area_atuacao) {
		this.area_atuacao = area_atuacao;
	}
	public String getModelo_trabalho() {
		return modelo_trabalho;
	}
	public void setModelo_trabalho(String modelo_Trabalho) {
		this.modelo_trabalho = modelo_Trabalho;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
	}
