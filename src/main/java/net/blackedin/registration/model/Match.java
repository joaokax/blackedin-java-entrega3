package net.blackedin.registration.model;

public class Match {

	private String resultado_contato;
	private int id_profissional;
	private int id_contato;
	
	public String getResultado_contato() {
		return resultado_contato;
	}
	public void setResultado_contato(String resultado_contato) {
		this.resultado_contato = resultado_contato;
	}
	public int getId_profissional() {
		return id_profissional;
	}
	public void setId_profissional(int id_profissional) {
		this.id_profissional = id_profissional;
	}
	public int getId_contato() {
		return id_contato;
	}
	public void setId_contato(int id_contato) {
		this.id_contato = id_contato;
	}


	

}
